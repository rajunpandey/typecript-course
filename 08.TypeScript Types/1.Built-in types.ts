/*
//2.string
//example
let first_name: string = 'rajun';
let last_name: string = "pandey";
let full_name: string = `${first_name} ${last_name}`;
*/

/*
//3.Boolean
// Boolean data type allows you to represent logical entities.
// It stores the either true or false value.
//example
let isReady: boolean = true;
*/

/*
//4.Symbol
//The symbol is a primitive data type, which is mostly used to create unique values
//Example
// Define a symbol
const UNIQUE_KEY = Symbol(); 
// Use the symbol as a property key in an object
let obj = {
[UNIQUE_KEY]: "SecretValue"
};
*/

/*
//5.Null
//The null type in TypeScript represents the intentional absence of any object value
//Example
let empty: null = null;
*/

/*
//6.Undefined
//The undefined data type represents the absence of value
//Example
let undef: undefined;
*/

/*
//7.Object
//The object is a non-primitive data type,
// which can contain any value that is not a number, string, boolean, symbol, null, or undefined. 
//Example
let person: object = {name: "rajun"};
*/

